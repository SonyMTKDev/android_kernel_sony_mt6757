static inline void push_table(struct LCM_setting_table *table, unsigned int count, unsigned char force_update){
	unsigned int i;

    for(i = 0; i < count; i++) {		
        unsigned cmd;
        cmd = table[i].cmd;
		
        switch (cmd) {
            case REGFLAG_DELAY :
                MDELAY(table[i].count);
                break;
				
            case REGFLAG_END_OF_TABLE :
                break;
				
            default:
				dsi_set_cmdq_V2(cmd, table[i].count, table[i].para_list, force_update);
       	}
    }
}

static inline void init_lcm_vdo_registers(void){
	//printk("%s - start - push_table - lcm_initialization_code_vdo_mode \n",__func__);
    push_table(lcm_initialization_code_vdo_mode, 
		sizeof(lcm_initialization_code_vdo_mode) / sizeof(struct LCM_setting_table), 1);
	//printk("%s - end - push_table - lcm_initialization_code_vdo_mode \n",__func__);
}

static inline void init_lcm_cmd_registers(void){
	int display_mode = 0;

	//printk("%s - start - push_table - lcm_initialization_code_cmd_mode \n",__func__);

	push_table(lcm_initialization_cmd_code_setting, 
		sizeof(lcm_initialization_cmd_code_setting) / sizeof(struct LCM_setting_table), 1);

	if(display_mode == DEBUG_MODE){
		push_table(lcm_initialization_cmd_code_setting, 
			sizeof(lcm_initialization_cmd_code_setting) / sizeof(struct LCM_setting_table), 1);	

		push_table(lcm_initialization_code_cmd_mode, 
			sizeof(lcm_initialization_code_cmd_mode) / sizeof(struct LCM_setting_table), 1);
	}
	//printk("%s - end - push_table - lcm_initialization_code_cmd_mode \n",__func__);
}

