static int td4322_probe(struct i2c_client *client, const struct i2c_device_id *id)
{  
	printk( "*********td4322_iic_probe\n");
	printk("********* KTD: info==>name=%s addr=0x%x\n",client->name,client->addr);
	td4322_i2c_client  = client;		
	return 0;      
}

static int td4322_remove(struct i2c_client *client)
{  	
	printk( "********* td4322_remove\n");
	td4322_i2c_client = NULL;
	i2c_unregister_device(client);
	return 0;
}

 /*static int ktd2151_write_bytes(unsigned char addr, unsigned char value)*/
int td4322_write_bytes(unsigned char addr, unsigned char value) 
{	
	int ret = 0;
	struct i2c_client *client = td4322_i2c_client;
	char write_data[2]={0};	
	write_data[0]= addr;
	write_data[1] = value;
    ret=i2c_master_send(client, write_data, 2);
	if(ret<0)
	printk("*********td4322 write data fail !!\n");	
	return ret ;
}

